namespace BioBreach.Core.Voxel
{
    /// <summary>
    /// 숙주 체내 복셀의 생체 조직 종류.
    /// 분해 도구(Dissolve Tool)로 파괴 시 각 타입에 따라 자원으로 환원됨.
    /// </summary>
    public enum VoxelType
    {
        Air            = 0,   // 공기        - 빈 공간 / 잠식된 영역
        Protein        = 1,   // 단백질      - 기본 근육 조직, 건설 재료
        Iron           = 2,   // 철분        - 혈관/혈액 조직, 구조 보강재
        Calcium        = 3,   // 칼슘        - 골격 조직, 기본 능력·골격 강화
        GeneticEssence = 4,   // 유전자 정수 - 희귀 핵산 물질, 특수 기술 재료
    }
}
